/** Class Calendar */
class Calendar {

    /**
     * Create a calendar object 
     * 
     * @constructor construct empty Calendar
     */
    constructor(title, lastUpdated, years, users) {
        this.title = title;
        this.lastUpdated = lastUpdated;
        this.years = years;
        this.users = users;
    }

}